namespace _08.Raw_Data
{
    public class Rectangle
    {
        private string id;
        private double width;
        private double height;
        private double horizontalCoordinate;
        private double verticalCoordinate;

        public string Id { get => id; set => id = value; }
        public double Width { get => width; set => width = value; }
        public double Height { get => height; set => height = value; }
        public double HorizontalCoordinate { get => horizontalCoordinate; set => horizontalCoordinate = value; }
        public double VerticalCoordinate { get => verticalCoordinate; set => verticalCoordinate = value; }



    }
}
